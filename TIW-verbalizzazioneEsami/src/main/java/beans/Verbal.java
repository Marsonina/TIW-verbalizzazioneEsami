package beans;

public class Verbal {
	private int verbalId;
	private String date;
	private String time;

	public int getVerbalId() {
		return verbalId;
	}

	public String getDate() {
		return date;
	}
	
	public String getTime() {
		return time;
	}
	
	public void setVerbalId(int i) {
		 verbalId = i;
	}

	public void setDate(String d) {
		date = d;
	}
	
	public void setTime(String t) {
		time = t;
	}
}
